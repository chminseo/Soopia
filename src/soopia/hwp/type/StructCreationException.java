package soopia.hwp.type;

public class StructCreationException extends Exception {

	public StructCreationException(String message, Throwable cause) {
		super(message, cause);
	}

	public StructCreationException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
